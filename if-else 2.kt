fun main() {
    
    //if-else statement
    val grade = 46
    if(grade>=90)
    {
        println("Grade: a")
    }
    else if(grade>=85)
    {
        println("Grade: B")
    }
    else if(grade>=70)
    {
        println("Grade: C")
    }
    else
    {
        println("Grade: D")
    }
    
}
